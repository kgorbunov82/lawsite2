// --- CRM (Leads) ---
export const getLeads = (): Lead[] => {
  try {
    const data = localStorage.getItem(KEYS.LEADS);
    return data ? JSON.parse(data) : [];
  } catch (e) {
    console.error("Failed to load leads", e);
    return [];
  }
};

export const addLead = (lead: Pick<Lead, 'name' | 'phone' | 'issue'>, source: 'form' | 'chat' = 'form'): Lead => {
  const leads = getLeads();
  const newLead: Lead = {
    ...lead,
    id: crypto.randomUUID(),
    date: new Date().toISOString(),
    status: LeadStatus.NEW,
    source
  };
  localStorage.setItem(KEYS.LEADS, JSON.stringify([newLead, ...leads]));
  return newLead;
};

export const updateLeadStatus = (id: string, status: LeadStatus): void => {
  const leads = getLeads();
  const updated = leads.map(l => l.id === id ? { ...l, status } : l);
  localStorage.setItem(KEYS.LEADS, JSON.stringify(updated));
};

export const deleteLead = (id: string): void => {
  const leads = getLeads();
  const filtered = leads.filter(l => l.id !== id);
  localStorage.setItem(KEYS.LEADS, JSON.stringify(filtered));
};

